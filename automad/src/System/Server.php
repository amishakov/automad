class Server {
	public static function url() {
		$protocol = 'http';
		$port = '';

		if (!empty($_SERVER['HTTPS']) && $_SERVER['HTTPS'] != 'off') {
			$protocol = 'https';
		}

		if (!in_array($_SERVER['SERVER_PORT'], array(80, 443))) {
			$port = ":$_SERVER[SERVER_PORT]";
		}

		return $protocol . '://' . $_SERVER['SERVER_NAME'] . $port;
	}
}
