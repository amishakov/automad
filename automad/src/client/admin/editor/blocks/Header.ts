// @ts-ignore
import Header from '@editorjs/header';
import { App, create } from '@/core';

export class HeaderBlock extends Header {
	/**
	 * Toolbox settings.
	 */
	static get toolbox() {
		return {
			title: App.text('heading'),
			icon: '<i class="bi bi-type-h1"></i>',
		};
	}

	/**
	 * Render the block.
	 *
	 * @return the rendered element
	 */
	render(): HTMLDivElement {
		const wrapper = create('div', ['cdx-block']);

		wrapper.appendChild(super.render());

		return wrapper;
	}
}
