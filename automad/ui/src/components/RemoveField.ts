import { classes, listen } from '../utils/core';
import { BaseComponent } from './Base';

/**
 * A button that remove its parent field.
 *
 * @extends BaseComponent
 */
class RemoveFieldComponent extends BaseComponent {
	/**
	 * The callback function used when an element is created in the DOM.
	 */
	connectedCallback(): void {
		listen(this, 'click', () => {
			this.closest(`.${classes.field}`).remove();
		});
	}
}

customElements.define('am-remove-field', RemoveFieldComponent);
